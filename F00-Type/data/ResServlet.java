/**
 * Servlet implementation class Fil
 */
@WebServlet("/" + RES_SERVLET)
public class ResServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse
	 *      response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
 		request.getRequestDispatcher(RES_JSP).forward(request, response);
 		request.getSession().removeAttribute("uploadedFiles");
 		request.getSession().removeAttribute("feilMelding");
	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse
	 *      response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		
		response.sendRedirect(UPLOAD_SERVLET);
	}

}
